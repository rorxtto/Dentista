    private void atualizaTabela(CadastroDao cadastroDao) {
        try {

            limparTabela();

            ArrayList<Cadastro> listaCadastros;
            listaCadastros = cadastroDao.consultar(); //consulta todos os registros da tabela Escola

            //Resgata o modelo da tabela            
            DefaultTableModel modeloTabela = (DefaultTableModel) Tb_dentista.getModel();

            for (Cadastro cadastro : listaCadastros) {
                String alergiaTexto = (cadastro.getAlergia() == 1) ? "Sim" : "Não";

                modeloTabela.addRow(new String[]{
                    Integer.toString(cadastro.getIdCad()),
                    cadastro.getNomecad(),
                    cadastro.getCpf(),
                    cadastro.getCro(),
                    cadastro.getCep(),
                    cadastro.getNumero(),
                    cadastro.getComplemento(),
                    cadastro.getTelefone(),
                    alergiaTexto, // Usar a variável alergiaTexto em vez do valor numérico
                    cadastro.getObs()
                });
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
        }
    }

    private void limparTabela() {
        //percorre a tabela e exclui todas as linhas
        while (Tb_dentista.getRowCount() > 0) {
            DefaultTableModel dm = (DefaultTableModel) Tb_dentista.getModel();
            dm.getDataVector().removeAllElements();
        }
    }

    private void buscarNome(CadastroDao cadastroDao) {
        try {

            limparTabela();

            ArrayList<Cadastro> listaCadastrosStr;
            listaCadastrosStr = cadastroDao.consultar(this.buscar.getText().trim());

            //Resgata o modelo da tabela            
            DefaultTableModel modeloTabela = (DefaultTableModel) Tb_dentista.getModel();

            for (Cadastro cadastro : listaCadastrosStr) {
                //adiciona em cada linha da tabela da tela o conteúdo de cada posição da listaCadastro
                modeloTabela.addRow(new String[]{Integer.toString(cadastro.getIdCad()),
                    cadastro.getNomecad(),
                    cadastro.getCpf(),
                    cadastro.getCro(),
                    cadastro.getTelefone(),});
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
        }
    }
