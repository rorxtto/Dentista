    private void jAltbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAltbtnActionPerformed
        if (Tb_dentista.getSelectedRow() != -1) {
            Cadastro cadastro = new Cadastro();
            cadastro.setIdCad(Integer.parseInt(jvId.getText()));
            cadastro.setNomeCad(jvNome.getText());
            cadastro.setCpf(jvCpf.getText());
            cadastro.setCro(jvCro.getText());
            cadastro.setCep(jvCEP.getText());
            cadastro.setNumero(jvNr.getText());
            cadastro.setComplemento(jvComplemento.getText());
            cadastro.setTelefone(jvTel.getText());
            cadastro.setAlergia(jvAlergia.getSelectedIndex());
            CadastroDao cadastroDao = new CadastroDao();
            cadastroDao.alterar(cadastro);
            atualizaTabela(cadastroDao);
            JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!", "", INFORMATION_MESSAGE);

            limparCampos();

            jvId.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cadastro para Alterar!");
        }
    }//GEN-LAST:event_jAltbtnActionPerformed

    private void jCadbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCadbtnActionPerformed
        if (jvCpf.getText().trim().isEmpty()
                || jvId.getText().trim().isEmpty()
                || jvNome.getText().trim().isEmpty()
                || jvCro.getText().trim().isEmpty()
                || jvCEP.getText().trim().isEmpty()
                || jvNr.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Dados Inválidos");
            jvId.requestFocus();
        } else {
            try {
                Cadastro cadastro = new Cadastro();
                cadastro.setIdCad(Integer.parseInt(jvId.getText()));
                cadastro.setNomeCad(jvNome.getText());
                cadastro.setCpf(jvCpf.getText());
                cadastro.setCro(jvCro.getText());
                cadastro.setCep(jvCEP.getText());
                cadastro.setNumero(jvNr.getText());
                cadastro.setComplemento(jvComplemento.getText());
                cadastro.setTelefone(jvTel.getText());
                cadastro.setAlergia(jvAlergia.getSelectedIndex());
                CadastroDao cadastroDao = new CadastroDao();
                cadastroDao.inserir(cadastro);
                // Limpa os campos de entrada e define o foco em jvId
                limparCampos();
                jvId.requestFocus();

                // Atualiza a tabela com os dados do banco de dados
                atualizaTabela(cadastroDao);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jCadbtnActionPerformed

    private void deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarActionPerformed
        try {
            int linhaSelecionada = Tb_dentista.getSelectedRow(); //resgata o número da linha selecionada na tabela

            if (linhaSelecionada == -1) {
                JOptionPane.showMessageDialog(null, "Selecione um Cadastro para remoção!", "ATENÇÃO!", WARNING_MESSAGE);
            } else {
                //resgata o conteúdo da primeira coluna da linha selecioanda
                this.jvId.setText(Tb_dentista.getValueAt(linhaSelecionada, 0).toString());

                //pergunda de confirmação antes de excluir o registro
                int opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja remover?");

                //se for igual a 0 siginifica que clicou em SIM
                if (opcao == 0) {
                    CadastroDao cadastroDao = new CadastroDao();
                    //chama o metodo excuirID passando o identificador do registro selecionado anteriormente
                    cadastroDao.excluirID(Integer.parseInt(this.jvId.getText()));

                    atualizaTabela(cadastroDao);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
        }
        limparCampos();
    }//GEN-LAST:event_deletarActionPerformed

